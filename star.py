import turtle
turtle.Screen().bgcolor("Aqua")
smita=turtle.Turtle()
smita.forward(100)
smita.left(120)
smita.forward(100)
smita.left(120)
smita.forward(100) 

smita.penup()
smita.right(150)
smita.forward(50)
smita.pendown()
smita.right(90)
smita.forward(100)
smita.right(120)
smita.forward(100)
smita.right(120)
smita.forward(100) 
turtle.done()